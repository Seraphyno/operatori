package operatori.relationali;

public class Relationali {
  
  public static void main(String[] args) {
    int i = 5, j = 7, k = 5;
    System.out.println(i < j); // afiseaza true
    System.out.println(i <= j); // afiseaza true
    System.out.println(i >= k); // afiseaza true
    System.out.println(i > k); // afiseaza false
  }
}
